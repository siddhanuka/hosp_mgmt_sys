    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String name = txtName.getText();
        if (name.isBlank()){
            JOptionPane.showMessageDialog(this, "Please enter the Name.");
            return;
        }

        long phone;
        try{
            phone = Long.parseLong(txtPhone.getText());
            if (txtPhone.getText().length() < 10) {
                JOptionPane.showMessageDialog(this, "Please enter a 10 digit number.");
                return;
            }
        }catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a number for Phone.");
            return;
        }

        int age;
        try{
            age = Integer.parseInt(txtAge.getText());
            if (age < 0) {
                JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
                return;
            }
        }catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
            return;
        }

        String sex = cmbSex.getSelectedItem().toString();
        if (sex.isBlank()){
            JOptionPane.showMessageDialog(this, "Please enter the Gender.");
            return;
        }

        String email = txtEmail.getText();
        if (!isValidEmail(email)){
            JOptionPane.showMessageDialog(this, "Please enter a valid Email.");
            return;
        }

        int id = getId();

        String role = cmbRole.getSelectedItem().toString();
        String user = txtUser.getText();
        if (user.isBlank()){
            JOptionPane.showMessageDialog(this, "Please enter the Username.");
            return;
        }        
        
        String pass = String.valueOf(txtPass.getText());
        if (pass.isBlank()){
            JOptionPane.showMessageDialog(this, "Please enter the Password.");
            return;
        }        
        
        if (role.equalsIgnoreCase("Patient")){
            Patient1 pat = new Patient1();
            pat.setAge(age);
            pat.setEmail(email);
            pat.setGender(sex);
            pat.setName(name);
            pat.setPassword(pass);
            pat.setPatId(id);
            pat.setPhone(phone);
            pat.setUserRole(role);
            pat.setUsername(user);
            patDir.addPatient(pat);
        }
        else if (role.equalsIgnoreCase("Doctor")){
            Doctor1 doc = new Doctor1();
            doc.setAge(age);
            doc.setEmail(email);
            doc.setGender(sex);
            doc.setName(name);
            doc.setPassword(pass);
            doc.setDocId(id);
            doc.setPhone(phone);
            doc.setUserRole(role);
            doc.setUsername(user);
            docDir.addDoctor(doc);
            cmbChooseDoc.addItem(doc.getName());
        }

        JOptionPane.showMessageDialog(this, "Record added successfully.");
        switchPanels(LoginPanel);
    }//GEN-LAST:event_btnAddActionPerformed

